#include "libft.h"
#include <limits.h>

int	ft_atoi(const char *str)
{
	size_t				i;
	int					sign;
	unsigned long int	num;

	i = 0;
	sign = 1;
	num = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' || str[i] == '\r'
		|| str[i] == '\v' || str[i] == '\f')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while ('0' <= str[i] && str[i] <= '9')
	{
		num = num * 10 + (str[i] - '0');
		i++;
	}
	if ((num > LONG_MAX) && (sign == -1))
		return ((int)LONG_MIN);
	else if (num > LONG_MAX)
		return ((int)LONG_MAX);
	else
		return ((int)num * sign);
}
